# Örnek bir liste oluşturalım
liste = ["a", "b", "c", "d", "e"]

# Liste uzunluğunu alalım
liste_uzunlugu = len(liste)

# Liste indekslerini oluşturalım ve yazdıralım
for indeks in range(liste_uzunlugu):
    print("İndeks:", indeks)
# bir liste içindeki indekslerin hepsini for döngüsü ile yazdırmak tek tek yazdırmaktan daha iyi
